namespace Fundamentos01;

public class OperadoresComparacao
{
    public void Executar()
    {
        Console.WriteLine("===Operadores de comparação===");

        int x = 8;
        int y = 8;

        Console.WriteLine($"x = {x}, y = {y}");
        Console.WriteLine($"x == y ? {x == y } (Igual a )");
        Console.WriteLine($"x != y ? {x != y } (Diferente )");
        Console.WriteLine($"x > y ? {x > y } (Maior que )");
        Console.WriteLine($"x < y ? {x < y } (Menor que )");
        Console.WriteLine($"x >= y ? {x >= y } (Maior ou igual que )");
        Console.WriteLine($"x <= y ? {x <= y } (Menor ou igual que )");




    }
    


} // Fim da class